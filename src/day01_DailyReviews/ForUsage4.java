package day01_DailyReviews;

public class ForUsage4 {
    
    /*
    
    find the sum of numbers between 100 - 200
     */

    public static void main(String[] args) {
        
        int total = 0;


        for (int i = 100; i <= 200 ; i++) {
            
            total += i;
            
        }

        System.out.println("total = " + total);
    }
}
